int mark[EXIT_ROW + 2][EXIT_COL + 2];

int stack_full()
{
	if (top >= MAX_STACK_SIZE - 1)
		return (TRUE);
	return (FALSE);
}

int stack_empty()
{
	if (top < -1)
		return (TRUE);
	return (FALSE);
}

void push(element x)
{
	if (stack_full())
	{
		printf("ERROR");
		return;
	}
	top++;
	stack[top] = x;
}

element pop()
{
	element x;
	if (stack_empty())
	{
		printf("ERROR");
		return (x);
	}
	x = stack[top--];
	return (x);
}

int path(const int startDir)
{
	int i, row, col, nextRow, nextCol, dir, found = FALSE, backtrack = -1;
	element position;
	mark[1][1] = 1;
	stack[0].row = 1;
	stack[0].col = 1;
	stack[0].dir = 0;
	top++;

	while (top > -1 && !found)
	{
		position = pop();
		backtrack++;
		row = position.row;
		col = position.col;
		dir = position.dir;

		while (dir < 8 && !found)
		{
			nextRow = row + move[(dir + startDir) % 8].row;
			nextCol = col + move[(dir + startDir) % 8].col;
			if (nextRow == EXIT_ROW && nextCol == EXIT_COL)
			{
				found = TRUE;
			}
			else if (!mark[nextRow][nextCol] && !maze[nextRow][nextCol])
			{
				mark[nextRow][nextCol] = 1;
				position.row = row;
				position.col = col;
				position.dir = dir + 1;
				push(position);
				row = nextRow;
				col = nextCol;
				dir = 0;
			}
			else
				dir++;
		}
	}
	return (backtrack);
}

void printResult(int startDir, int backtrack)
{
	for (int i = 0; i <= top; i++)
		mark[stack[i].row][stack[i].col] = 2 + stack[i].dir;
	wprintf(ANSI_COLOR_GREEN L"Start direction: %d(%lc)\nTotal backtracking: %d\n" ANSI_COLOR_RESET, startDir, arrow[startDir], backtrack);
	for (int i = 0; i < EXIT_ROW + 2; i++)
	{
		for (int j = 0; j < EXIT_COL + 2; j++)
		{
			if (maze[i][j] == 1)
				printf("■ ");
			else if (mark[i][j] > 2)
			{
				int tmpDir = (mark[i][j] + startDir - 2) % 8;
				tmpDir = tmpDir ? tmpDir - 1 : 7;
				wprintf(ANSI_COLOR_GREEN L"%lc" ANSI_COLOR_RESET " ", arrow[tmpDir]);
			}
			else if (mark[i][j] == 1)
				printf(ANSI_COLOR_MAGENTA "□ " ANSI_COLOR_RESET);
			else
				printf("  ");
		}
		printf("\n");
	}
	printf("Stack info\n");
	for (int i = 0; i <= top; i++)
		wprintf(L"(%d,%d), %d(%lc)\n", stack[i].row, stack[i].col, (stack[i].dir + startDir) % 8, arrow[(stack[i].dir + startDir) % 8]);
	printf("\n\n");
}

void initialize()
{
	for (int i = 0; i < EXIT_ROW + 2; i++)
		for (int j = 0; j < EXIT_COL + 2; j++)
			mark[i][j] = 0;
	top = -1;
}

